    def get_school_coordinates(self, name, info_link):
        """
        Okul 'Bilgi' sayfasÄ±na gidip, /tema/harita.php iÃ§indeki
        Google Maps JavaScriptâ€™inden LatLng(...) deÄŸerini Ã§ekmeye Ã§alÄ±ÅŸÄ±r.
        q= parametresi iÅŸe yaramazsa buradaki javascript tanÄ±mÄ±nÄ± parse eder.
        """
        import re

        # 1) map sayfasÄ± URLâ€™si
        if "tema/harita.php" in info_link:
            map_url = info_link
        else:
            map_url = info_link.rstrip("/") + "/tema/harita.php"

        try:
            # 2) SayfayÄ± indir
            resp = requests.get(map_url, timeout=10)
            resp.raise_for_status()
            text = resp.text

            # 3) Ã–nce iframe src iÃ§indeki q=lat,lonâ€™u dene
            m = re.search(r"embed/v1/place\?[^\"']*q=([0-9\.\-]+),([0-9\.\-]+)", text)
            if m:
                return float(m.group(1)), float(m.group(2))

            # 4) Fallback: JavaScript iÃ§inde LatLng(lat, lon) tanÄ±mÄ±nÄ± ara
            m2 = re.search(r"LatLng\(\s*([0-9\.\-]+)\s*,\s*([0-9\.\-]+)\s*\)", text)
            if m2:
                return float(m2.group(1)), float(m2.group(2))

        except Exception as e:
            # Ä°steÄŸe baÄŸlÄ±: konsola hata bas
            print(f"[get_school_coordinates] {name} hata: {e}")

        # Bulunamazsa None dÃ¶ndÃ¼r
        return None, None
